const radius = 5; //We use const here as the radius of a circle is constant
let area = Math.PI * Math.pow(radius, 2); //We use let here as the area of a circle changes with equation
let perimeter = 2 * Math.PI * radius; //We use let here as the perimeter of a circle changes with equation

console.log("Area of circle", area); //logging the area
console.log("Perimeter of circle", perimeter); //logging the perimeter